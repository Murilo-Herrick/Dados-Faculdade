package exercicio_03;

public class Figura2D {
	
	
	private int base;
	private int altura;
		
	public Figura2D(int base, int altura) {
		this.base = base;
		this.altura = altura;
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
}
